// Description: For loop construct in JavaScript

// Note
// 1. Initialization (Statement 1): The statement var i = 0; is executed before the loop (ie the code block) starts.
// 2. Condition (Statement 2): The statement var i < 10; defines the condition for running the loop (ie the code block).
// 3. Increment (Statement 3): The statement i++; is executed each time after the loop (ie the code block) execution.
// 4, All 3 statements above are optional.
// 5. If the condition (Statement 2) is omitted, then a condition of true is assumed.
for ( var i = 0; i < 10; i++) {
    console.log("i = ", i);
}
